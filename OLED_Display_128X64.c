void OLED_Display_Init()
{
	uint8_t displayoff[2] = {0x80, 0xAE};
	uint8_t displayon[2] = {0x80 , 0xAF};
	uint8_t normal_display[2] = {0x80 , 0xA6};
	uint8_t set_memory_mode[2] = {0x80, 0x20};          
    uint8_t horizontal_addressing[2] = {0x80, 0x00};    
    uint8_t set_flip_mode[2] = {0x80, 0xC8};
	uint8_t segment_remap[2] = {0x80, 0xA1 | 0x01};
	HAL_I2C_Master_Transmit(&BaseShield_I2C_Struct, OLED_DISPLAY_ADDR, displayoff, 2, 0xFFFF);
    HAL_I2C_Master_Transmit(&BaseShield_I2C_Struct, OLED_DISPLAY_ADDR, set_memory_mode, 2, 0xFFFF);
    HAL_I2C_Master_Transmit(&BaseShield_I2C_Struct, OLED_DISPLAY_ADDR, horizontal_addressing, 2, 0xFFFF);
    HAL_I2C_Master_Transmit(&BaseShield_I2C_Struct, OLED_DISPLAY_ADDR, set_flip_mode, 2, 0xFFFF);
    HAL_I2C_Master_Transmit(&BaseShield_I2C_Struct, OLED_DISPLAY_ADDR, normal_display, 2, 0xFFFF);
    HAL_I2C_Master_Transmit(&BaseShield_I2C_Struct, OLED_DISPLAY_ADDR, segment_remap, 2, 0xFFFF);
	OLED_clearDisplay();
	HAL_I2C_Master_Transmit(&BaseShield_I2C_Struct, OLED_DISPLAY_ADDR, displayon, 2, 0xFFFF);
}

void OLED_sendCommand(uint8_t command)
{
	uint8_t Command[2] = {0x80, command};
	HAL_I2C_Master_Transmit(&BaseShield_I2C_Struct, OLED_DISPLAY_ADDR, Command, 2, 0xFFFF);
}

void OLED_sendData(uint8_t data)
{
	uint8_t Data[2] = {0x40, data};
	HAL_I2C_Master_Transmit(&BaseShield_I2C_Struct, OLED_DISPLAY_ADDR, Data, 2, 0xFFFF);
}

void OLED_setTextXY(uint8_t row, uint8_t column)
{
	OLED_sendCommand(0xB0 + row);
	OLED_sendCommand(0x00 + (8 * column & 0x0F));
	OLED_sendCommand(0x10 + ((8 * column >> 4) & 0x0F));
}

void OLED_clearDisplay()
{
	for (int i = 0; i < 8; i++)
	{
		OLED_setTextXY(i,0);
		for (int j = 0; j < 16; j++)
		{
			OLED_putChar(' ');
		}
	}
	OLED_setTextXY(0,0);
}

void OLED_putChar(char C)
{
	if (C < 32 || C > 127)
	{
		C = 32;
	}
	for (int i = 0; i < 8; i++) 
	{
		int j = (uint8_t)C - 32;
        OLED_sendData(BasicFont[j][i]);
    }
}

void OLED_putStr(uint8_t *pData, uint16_t Size)
{
	OLED_setTextXY(0,0);
	for (int i = 0; i < Size; i++)
	{
		OLED_putChar(pData[i]);
	}
}

void OLED_putPicture(uint8_t *picture)
{
	OLED_setTextXY(0,0);
	for (int i = 0; i < 1024; i++)
	{
		OLED_sendData(picture[i]);
	}
}


void OLED_putStr_fast(uint8_t *pData, uint16_t Size)
{
    uint8_t row = 0;
    uint8_t col = 0;
    OLED_setTextXY(row, col); 

    for (int i = 0; i < Size && i < 128; i++)
    {
        if (pData[i] != OLED_UART_Buffer[i])
        {
            OLED_putChar(pData[i]); 
        }
        col++;
        if (col >= 16) 
		{
            col = 0;     
            row++;      
            if (row >= 8) 
			{
                row = 0;
            }
            OLED_setTextXY(row, col); 
        } 
		else 
		{
            OLED_setTextXY(row, col);
        }
    }
    memcpy(OLED_UART_Buffer, pData, Size);
}
